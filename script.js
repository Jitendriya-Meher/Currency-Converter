// conersion
const convert = async () => {
    // create references
    const amount = document.querySelector("#amount");
    const from = fromDropdown.value;
    const to = toDropdown.value;

    if( amount.value.length == 0){
        alert("Please fill in the amount");
        return;
    }

    const respone = await fetch(url);
    const data = await respone.json();
    console.log(data);

    let fromExchange = data.conversion_rates[from];
    let toExchange = data.conversion_rates[to];

    let convertedAmount = (amount.value / fromExchange) * toExchange;

    const result = document.querySelector(".result");
    result.innerHTML = `${amount.value} ${from} = ${convertedAmount.toFixed(2)} ${to}`
}
convert();

const btn = document.querySelector("#convert");
